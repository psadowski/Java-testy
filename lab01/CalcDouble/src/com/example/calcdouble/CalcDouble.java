package com.example.calcdouble;

public class CalcDouble {
	
	public double addD(double n1, double n2){
		return n1 + n2;
	}
	
	public double subD(double n1, double n2){
		return n1 - n2;
	}
	
	public double multD(double n1, double n2){
		return n1 * n2;
	}
	
	public double divD(double n1, double n2){
		return n1 / n2;
	}
	
	public boolean greaterD(double a, double b){
		return a > b;
	}
}
